        )
    } else {
        LandscapeUiProfile(
            isCompactHeight = false,
            outerHorizontalPadding = 12.dp,
            outerVerticalPadding = 10.dp,
            contentGap = 12.dp,
            mainInnerHorizontalPadding = 6.dp,
            mainInnerVerticalPadding = 4.dp,
            sidebarWidth = 62.dp,
            sidebarVerticalPadding = 12.dp,
            sidebarHorizontalPadding = 6.dp,
            sidebarIconSpacing = 12.dp,
            sidebarIconSize = 26.dp,
