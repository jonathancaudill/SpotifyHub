@Composable
fun rememberLandscapeUiProfile(
    maxWidth: Dp,
    maxHeight: Dp,
): LandscapeUiProfile = remember(maxWidth, maxHeight) {
    resolveLandscapeUiProfile(maxWidth, maxHeight)
}
