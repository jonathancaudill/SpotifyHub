        minOf(width, height) <= 540.dp

    return if (isCompactHeight) {
        LandscapeUiProfile(
            isCompactHeight = true,
            outerHorizontalPadding = 8.dp,
            outerVerticalPadding = 8.dp,
            contentGap = 8.dp,
            mainInnerHorizontalPadding = 4.dp,
