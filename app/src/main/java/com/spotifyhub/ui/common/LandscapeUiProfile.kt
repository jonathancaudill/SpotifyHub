            typeScale = 1.08f,
            nowPlayingMetaWeight = 0.56f,
            nowPlayingArtworkWeight = 0.44f,
            nowPlayingHeroGap = 12.dp,
            nowPlayingSectionGap = 12.dp,
            nowPlayingProgressToUtilityGap = 14.dp,
            nowPlayingTitleSize = 38.sp,
            nowPlayingTitleLineHeight = 42.sp,
            nowPlayingTitleMaxLines = 2,
            nowPlayingArtistSize = 22.sp,
            nowPlayingAlbumSize = 17.sp,
            progressLabelSize = 12.sp,
            transportSideButtonSize = 68.dp,
            transportCenterButtonSize = 82.dp,
            transportGap = 24.dp,
