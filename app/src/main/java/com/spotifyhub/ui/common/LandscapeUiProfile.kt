            nowPlayingSectionGap = 26.dp,
            nowPlayingProgressToUtilityGap = 12.dp,
            nowPlayingTitleSize = 34.sp,
            nowPlayingTitleLineHeight = 38.sp,
            nowPlayingTitleMaxLines = 2,
            nowPlayingArtistSize = 20.sp,
            nowPlayingAlbumSize = 16.sp,
            progressLabelSize = 11.sp,
            transportSideButtonSize = 72.dp,
